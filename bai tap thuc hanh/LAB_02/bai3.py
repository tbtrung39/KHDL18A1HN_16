thu = input("Nhập vào một số (1-7) để xác định thứ trong tuần: ")
if thu == 1:
    print("Thứ bạn đã nhập là: Sunday")
elif thu == 2:
    print("Thứ bạn đã nhập là: Monday")
elif thu == 3:
    print("Thứ bạn đã nhập là: Tuesday")
elif thu == 4:
    print("Thứ bạn đã nhập là: Wednesday")
elif thu == 5:
    print("Thứ bạn đã nhập là: Thursday")
elif thu == 6:
    print("Thứ bạn đã nhập là: Friday")
elif thu == 7:
    print("Thứ bạn đã nhập là: Saturday")
else:
    print("Tháng không hợp lệ. Vui lòng nhập lạilại!")