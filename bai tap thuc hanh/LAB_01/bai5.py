x_a= float(input("Nhập hoành độ x của vecto a: ")) 
y_a= float(input("Nhập tung độ y của vecto a: ")) 
x_b = float(input("Nhập hoành độ x của vecto b: ")) 
y_b = float(input("Nhập tung độ y của vecto b: ")) 
tich_vo_huong = x_a * x_b + y_a * y_b
print('tích vô hướng của vecto a và b là: ', tich_vo_huong)