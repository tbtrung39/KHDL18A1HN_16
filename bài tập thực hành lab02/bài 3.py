thu_trong_tuan = int(input("Nhập vào các thứ (1->7) trong tuần: "))
if  thu_trong_tuan == 1:
    print("1:Monday")
elif thu_trong_tuan  == 2:
    print("2:Tuesday")
elif  thu_trong_tuan == 3:
    print("3:Wednesday")
elif thu_trong_tuan  == 4:
    print("4:Thursday")
elif thu_trong_tuan  == 5:
    print("5:Friday")
elif thu_trong_tuan  == 6:
    print("6:Saturday")
elif thu_trong_tuan  == 7:
    print("Sunday")
else :
    print("Nhập sai vui lòng nhập lại!")
