thu_trong_tuan=int(input("Nhập vào thứ trong tuần:"))
if thu_trong_tuan ==1:
    print("1.sunday")
elif thu_trong_tuan == 2:
    print("2.monday")
elif thu_trong_tuan == 3:
    print("3.tuesday")  
elif thu_trong_tuan == 4:
    print("4.wednessday")
elif thu_trong_tuan == 5:
    print("5.thursday")
elif thu_trong_tuan == 6:
    print("6.friday")
elif thu_trong_tuan == 7:
    print("7.saturday")
else:
    print("Thứ không hợp lệ. Vui lòng nhập lại")