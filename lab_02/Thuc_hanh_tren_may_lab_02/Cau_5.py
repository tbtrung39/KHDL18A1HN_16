# Nhập tháng từ người dùng
thang = int(input("Nhập vào tháng (1-12): "))

# Kiểm tra và hiển thị kết quả
if thang == 1:
    print("Tháng 1 là January.")
elif thang == 2:
    print("Tháng 2 là February.")
elif thang == 3:
    print("Tháng 3 là March.")
elif thang == 4:
    print("Tháng 4 là April.")
elif thang == 5:
    print("Tháng 5 là May.")
elif thang == 6:
    print("Tháng 6 là June.")
elif thang == 7:
    print("Tháng 7 là July.")
elif thang == 8:
    print("Tháng 8 là August.")
elif thang == 9:
    print("Tháng 9 là September.")
elif thang == 10:
    print("Tháng 10 là October.")
elif thang == 11:
    print("Tháng 11 là November.")
elif thang == 12:
    print("Tháng 12 là December.")
else:
    print("Tháng không hợp lệ! Vui lòng nhập số từ 1 đến 12.")