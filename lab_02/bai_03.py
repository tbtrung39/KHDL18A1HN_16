day = int(input("Nhập vào một số từ 1 đến 7: "))

if day == 1:
    print("Chủ Nhật")
elif day == 2:
    print("Thứ Hai")
elif day == 3:
    print("Thứ Ba")
elif day == 4:
    print("Thứ Tư")
elif day == 5:
    print("Thứ Năm")
elif day == 6:
    print("Thứ Sáu")
elif day == 7:
    print("Thứ Bảy")
else:
    print("Số nhập vào không hợp lệ!")