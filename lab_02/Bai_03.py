while True:
    day = int(input("Nhập số thứ (1-7): "))
    
    if day == 1:
        print("Sunday")
        break
    elif day == 2:
        print("Monday")
        break
    elif day == 3:
        print("Tuesday")
        break
    elif day == 4:
        print("Wednesday")
        break
    elif day == 5:
        print("Thursday")
        break
    elif day == 6:
        print("Friday")
        break
    elif day == 7:
        print("Saturday")
        break
    else:
        print("Số không hợp lệ, vui lòng nhập lại!")
