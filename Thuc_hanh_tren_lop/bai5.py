a_x = float(input("Nhập tọa độ x của vector a: "))
a_y = float(input("Nhập tọa độ y của vector a: "))
b_x = float(input("Nhập tọa độ x của vector b: "))
b_y = float(input("Nhập tọa độ y của vector b: "))
tich_vo_huong = a_x * b_x + a_y * b_y
print("Tích vô hướng của hai vector:", round(tich_vo_huong, 2))