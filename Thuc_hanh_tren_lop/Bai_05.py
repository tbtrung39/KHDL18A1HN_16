# Nhập thành phần vector a
a1 = float(input("Nhập a1 của vector a: "))
a2 = float(input("Nhập a2 của vector a: "))
a3 = float(input("Nhập a3 của vector a: "))

# Nhập thành phần vector b
b1 = float(input("Nhập b1 của vector b: "))
b2 = float(input("Nhập b2 của vector b: "))

b3 = float(input("Nhập b3 của vector b: "))

# Tích vô hướng của 2 vector
tich_vo_huong = a1 * b1 + a2 * b2 + a3 * b3

# In kết quả
print(f"Tích vô hướng của 2 vector a và b là: {tich_vo_huong}")
