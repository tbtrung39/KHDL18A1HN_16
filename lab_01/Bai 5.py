ax = float(input("Nhap toa do x cua vector a: "))
ay = float(input("Nhap toa do y cua vector a: "))
az = float(input("Nhap toa do z cua vector a: "))
bx = float(input("Nhap toa do x cua vector b: "))
by = float(input("Nhap toa do y cua vector b: "))
bz = float(input("Nhap toa do z cua vector b: "))
tich_vo_huong = ax * bx + ay * by + az * bz
print(f"Tich vo huong cua hai vector: {tich_vo_huong:.2f}")