choice = int(input("Nhập vào các thứ (1->7): "))
if choice == 1:
    print("1:Monday")
elif choice == 2:
    print("2:Tuesday")
elif choice == 3:
    print("3:Wednesday")
elif choice == 4:
    print("4:Thursday")
elif choice == 5:
    print("5:Friday")
elif choice == 6:
    print("6:Saturday")
elif choice == 7:
    print("Sunday")
else :
    print("Nhập sai vui lòng nhập lại!")